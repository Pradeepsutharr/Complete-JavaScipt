//Assignemnt Operators-------------------------

/*
=	x = y	    x = y
+=	x += y	    x = x + y
-=	x -= y	    x = x - y
*=	x *= y	    x = x * y
/=	x /= y	    x = x / y
%=	x %= y	    x = x % y
**=	x **= y	    x = x ** y
*/

